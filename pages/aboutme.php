<h5>Frameworks</h5>
<p>
	Most webapplications share a lot of functionality and to avoid having to reinvent the wheel everytime we use libraries or bundles of libraries called frameworks to a strong foundation for our webapplication. <br />
	With so many open source communities building and maintaining frameworks having a homebrew framework nowadays is like a upstream battle. <br />
	In the past 2 years I've gone from maintaining a homebrew framework to exploring almost all major PHP frameworks out there! <br />
	<br />
	Though I've tried many the one I've come to love is <i>Symfony2</i> since this is (curently) the framework that's the most elegant and just using it forces you to become a better programmer. <br />
	There are many more out there such as Yii, Fuel, Flow, Lithium. etc. and I'm sure I could also learn to love those if I spend some time working with those! <br />
	<ul>
		<li>Symfony2 (PHP)</li>
		<li>ExpressJS (NodeJS)</li>
		<li>Djangode (NodeJS)</li>
	</ul>
</p>

<h5>CMS</h5>
<p>
	The choice between starting a webapplication based on a framework or a CMS (or from scratch even) can be a tough one and is almost as important as the choice of the programming language (or maybe even more important!). <br />
	Using a CMS might give you a lot of out-of-the-box functionality but if you won't be using 80% of it then it will (unnecessarly) slow your webapplication down. <br />
	Most open source CMS out there also enforce a lot less 'pretty' programming and achitecture! <br />
	But their communities supply you with a lot of (free) modules which can save you weeks of development! <br />
	<ul>
		<li>Drupal</li>
		<li>Joomla</li>
		<li>Wordpress</li>
	</ul>
</p>

