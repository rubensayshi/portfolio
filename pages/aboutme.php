	Especially NodeJS has real nice chemistry with NoSQL database since these are also often non-blocking! <br />
	<ul>
		<li>MySQL (SQLLite, MSSQL, Access)</li>
		<li>MongoDB, CouchDB</li>
	</ul>
</p>
