<h5>Server software</h5>
<p>
	There are so many tools available to be installed on your servers that I could never list them all so curently the the most important are by far the following; <br />
	<i>Linux</i> is at the base of most webservers beating Windows servers by far and I've grown to love <i>Linux</i> so much that I've even switched from working on Windows to Ubuntu on my (work)laptop. <br />
	<i>Apache2</i> is the piece of software which lives between the requests going to your webserver and your actual webapplication and you should know it by heart. <br />
	<i>Varnish</i> is a reverse proxy which allows you to cache webpages on high trafic websites. Developing a webapplication which will run behind a reverse proxy either requires a perfect setup of your webapplications (with correct headers and such) or if you're running a (for caching) horible system like Drupal you will have to trick both systems into working together! <br />
	Last is <i>Apache SOLR</i>, a very advanced (and FAST!) search engine which can be configured to fit all you'll ever need in a search engine. <br />
	<ul>
		<li>Linux (Ubuntu, CentOS)</li>
		<li>Apache2</li>
		<li>Varnish</li>
		<li>Apache SOLR</li>
	</ul>
</p>

